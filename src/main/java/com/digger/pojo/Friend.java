package com.digger.pojo;

public class Friend {
    private Integer id;

    private Integer friendint;

    private Byte state;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getFriendint() {
        return friendint;
    }

    public void setFriendint(Integer friendint) {
        this.friendint = friendint;
    }

    public Byte getState() {
        return state;
    }

    public void setState(Byte state) {
        this.state = state;
    }
}